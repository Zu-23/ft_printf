#include "ft_printf.h"

int	ft_putnbr(long a)
{
	static int i;

	if (a < 0)
	{
		a *= -1;
		write (1, "-", 1);
		i++;
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	if (a <= 9)
	{
		a += '0';
		write (1, &a, 1);
		i++;
	}
	return (i);
}